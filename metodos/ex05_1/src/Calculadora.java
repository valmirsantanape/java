public class Calculadora {
    public static float soma(int a, int b){
        return a + b;
    }

    public static float subtrair(int a, int b){
        return a - b;
    }

    public static float multiplicar(int a, int b){
        return a * b;
    }

    public static float dividir(int a, int b){
        return a / b;
    }

}
